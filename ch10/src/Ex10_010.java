import java.text.SimpleDateFormat;
import java.util.Date;

/*
 * 날짜: 2021/05/27
 * 내용: 자바의 정석_형식화 클래스(SimpleDateFormat)
 * 이름: 김하린
 * 날짜와 시간을 다양한 형식으로 출력할 수 있게 해줌
 */
public class Ex10_010 {

	public static void main(String[] args) {

		Date today = new Date();
		
		SimpleDateFormat sdf1, sdf2, sdf3, sdf4;
		SimpleDateFormat sdf5, sdf6, sdf7, sdf8, sdf9;
		
		sdf1 = new SimpleDateFormat("yyyy-MM-dd"); //M:월, m:분
		sdf2 = new SimpleDateFormat("''yy년 MM dd일 E요일");
		sdf3 = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
		sdf4 = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss a");
		
		sdf5 = new SimpleDateFormat("오늘은 올 해의 D번째 날입니다."); //년의 몇 번째 일
		sdf6 = new SimpleDateFormat("오늘은 이 달의 d번째 날입니다."); //월의 몇 번째 일
		sdf7 = new SimpleDateFormat("오늘은 올 해의 w번째 주입니다."); //년의 몇 번째 주
		sdf8 = new SimpleDateFormat("오늘은 이 달의 W번째 주입니다."); //월의 몇 번째 주
		sdf9 = new SimpleDateFormat("오늘은 이 달의 F번째 E요일입니다."); //월의 몇 번째 요일(1~5)
		
		System.out.println(sdf1.format(today));
		System.out.println(sdf2.format(today));
		System.out.println(sdf3.format(today));
		System.out.println(sdf4.format(today));
		System.out.println();
		System.out.println(sdf5.format(today));
		System.out.println(sdf6.format(today));
		System.out.println(sdf7.format(today));
		System.out.println(sdf8.format(today));
		System.out.println(sdf9.format(today));
	}
}
