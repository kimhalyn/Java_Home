public class Ex3_003 {

	public static void main(String[] args) {
		
		int i = 5, j = 5;
		System.out.println(i++); //출력하고 증가
		System.out.println(++j); //증가하고 출력
		System.out.println("i = " + i + ", j = " + j); //둘다 증가된 상태로 출력
		
	}
	
}
